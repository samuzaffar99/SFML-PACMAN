void Map::draw(RenderWindow& window) const {
	for (int i = 0; i < ROWS; ++i) {
		for (int j = 0; j < COLS; ++j) {
			if (grid[i][j] == 1) {
				sf::RectangleShape wall(sf::Vector2f(TILE_SIZE, TILE_SIZE));
				wall.setFillColor(sf::Color::Blue);
				wall.setPosition(j * 15.f, i * 15.f + 45.f);
				window.draw(wall);
			}
		}
	}
}

bool Map::isFood(int row, int col) const {
	return grid[row][col] == 2;
}


bool Map::isWall(int row, int col) const {
	return grid[row][col] == 1;
}
